    private void SaveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBTNActionPerformed
        // TODO add your handling code here:
        if(ISBNField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "El ISBN es Requerido", "Validacion", JOptionPane.WARNING_MESSAGE); 
            ISBNField.requestFocusInWindow();
            return;
        }
        if(TituloField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "El titulo es Requerido", "Validacion", JOptionPane.WARNING_MESSAGE); 
            TituloField.requestFocusInWindow();
            return;
        }
        if(AutorField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "El Autor es Requerido", "Validacion", JOptionPane.WARNING_MESSAGE); 
            AutorField.requestFocusInWindow();
            return;
        }
        if(DescField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "La descripcion es Requerida", "Validacion", JOptionPane.WARNING_MESSAGE); 
            DescField.requestFocusInWindow();
            return;
        }
        if(EditorialField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "La editorial es Requerida", "Validacion", JOptionPane.WARNING_MESSAGE); 
            EditorialField.requestFocusInWindow();
            return;
        }
        if(EditionField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "La edicion es Requerida", "Validacion", JOptionPane.WARNING_MESSAGE); 
            EditionField.requestFocusInWindow();
            return;
        }
        if(QtyField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "La cantidad es Requerida", "Validacion", JOptionPane.WARNING_MESSAGE); 
            QtyField.requestFocusInWindow();
            return;
        }
        if(GeneroField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "El Genero es Requerido", "Validacion", JOptionPane.WARNING_MESSAGE); 
            GeneroField.requestFocusInWindow();
            return;
        }
        if(StatusField.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "El estado es Requerido", "Validacion", JOptionPane.WARNING_MESSAGE); 
            StatusField.requestFocusInWindow();
            return;
        }
        
        String msg = null;
        if(!ISBNField.getText().equals("")){
            DLibros dts = new DLibros();
            LLibros fun = new LLibros();
            dts.setISBN(ISBNField.getText());
            dts.setTitulo(TituloField.getText());
            dts.setDescripcion(DescField.getText());
            dts.setIdEditorial(Integer.parseInt(idEF.getText()));
            dts.setId_autor(Integer.parseInt(IDAField.getText()));
            dts.setEdicion(EditionField.getText());
            dts.setCantidad(Integer.parseInt(QtyField.getText()));
            dts.setId_genero(Integer.parseInt(idGF.getText()));
            dts.setIdEstadoLibro(Integer.parseInt(StatusField.getText()));
            msg = fun.insertarLibro(dts);
            if(msg == "si"){
               JOptionPane.showMessageDialog(rootPane, "Registrado Satisfactoriamente", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }else
            {
                JOptionPane.showMessageDialog(rootPane, "Uh oh, ocurri√≥ un error", "Info", JOptionPane.INFORMATION_MESSAGE); 
            }
        }
        clearField();
    }//GEN-LAST:event_SaveBTNActionPerformed

